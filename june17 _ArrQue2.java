package june17;

public class ArrQue2 {

	public static void main(String[] args) {
		//adding 1 to each matrix element
		int[][] arr = {{1,2,3,4},{2,3,4,5},{5,6,7,8},{3,5,7,9}};

		for(int i=0;i<4;i++) {
			for(int j=0;j<4;j++) {
				System.out.print(arr[i][j]+1+" ");
			
		}
			System.out.println();
			}
		

	}

}